  <h5>Vacaciones de invierno.</h5>
  <div class="clear"></div>
    <p> 
    Ya todos sabemos que las vacaciones de invierno son de los niños, ellos son los que más disfrutarán de estas dos semanas que les permiten, relajarse,  disfrutar  y en algunos casos también aprender,  pero no para pocos  padres llenar este periodo de tiempo,  puede   transformarse en una gran desafío.
    </p>
    <p> 
    Para eso te dejamos algunas ideas imperdibles:
    </p>
    <p> 
    <strong>Parque Bicentenario de la Infancia</strong>: El nuevo parque puedes realizar actividades al aire libre, visitar la cascada con 60 toboganes, el anfiteatro, espacio de títeres y casa en los árboles, la reja túnel 3D, la plaza con juegos de agua, y muchos más juegos infantiles.  Martes a Domingo y Festivos de 10:00 a 17:00 horas (cierre acceso público, a las 18hrs. se cierra el parque) En la ladera poniente del Cerro San Cristóbal. 
    </p>
    
    <p> 
    <strong>Valle nevado</strong>: vive la experiencia de un día de esquí en familia y de todas las actividades que se están realizando en Valle Nevado, además podrás conocer la nueva Telecabina de montaña que recorrerá  acerca de 1000 metros de longitud y tendrá capacidad para 6 personas. Más información en www.vallenevado.cl
    </p>

    <p> 
    <strong>El Planetario</strong>: En estas vacaciones los visitantes de Planetario encontrarán funciones de audiovisuales astronómicos en el domo principal, Cine 3D con dos nuevos títulos “Bajo el Mar” (del 6 al  13) y “Dinosaurios Alive” (del 14 al 21), talleres manuales, juegos, experimentos en vivo del profesor Tuerca Loca y mucho más. Desde el 6 al 21 de Julio, horario desde 11:00 a 18:00 horas.  Av. L. Bdo. O'Higgins N°3349, Estación Central.
    </p>

    <p> 
    <strong>El Museo Interactivo Mirador MIM</strong>: es uno de los más visitados del país. Cuenta con 7.200 mts cuadrados y se sitúa en el parque público Brasil, en la comuna de la Granja, Santiago. Es un espacio educativo, lúdico e interactivo. Promete a los visitantes una experiencia que incluye ciencia, arte y tecnología. Más información en www.mim.cl
    </p>
    <p>
    Actividades Culturales: A lo largo del territorio nacional se están realizando más de 300 actividades, durante estas vacaciones de invierno. El Ministerio de la Cultura las recopiló en el sitio www.cultura.gob.cl 
    </p>
